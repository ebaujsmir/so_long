#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	ft_putstr_fd(s, fd);
	ft_putchar_fd('\n', fd);
}

/*
* DESCRIPTION
*	This function writes the string s to the file descriptor fd followed by a
*	'\n' to make a new line.
*/